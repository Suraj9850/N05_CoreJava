package org.tnsif.WrapperClassDemo;
//Program to demonstrate on autoboxing
//Autoboxing nothing but a conversion on primitive to object type
public class AutoBoxing {

	public static void main(String[] args) {
		
		char c='A';
		//converting premitive datatype to object type
		Character ch=c;
		System.out.println(ch);
	}

}
